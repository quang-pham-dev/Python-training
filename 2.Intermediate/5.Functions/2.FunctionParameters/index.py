# Function Parameter(s)
# Function parameters are values that are passed into a function when it is called.
# They allow functions to take input, making them more versatile and adaptable.


# Example 1:
def greet(name):
    print(f"Hello, {name}!")


greet("Quang")  # Output: Hello, Quang!


# Example 2:
def calculate_area(length, width):
    return length * width


area = calculate_area(5, 3)
print(
    f"The area of the rectangle is: {area}"
)  # Output: The area of the rectangle is: 15
