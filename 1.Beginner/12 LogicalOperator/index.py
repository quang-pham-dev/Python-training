# Logical operators are used to perform logical operations on boolean values. 
# These operators allow you to combine or modify boolean values and make more complex decisions in your code.

print(True and True)
print(True and False)

print(True or True)
print(True or False)

print(not True)
print(not False)

x = True
y = False
result = x and y
print(result)

result = x or y 
print(result)

result = not y 
print(result)